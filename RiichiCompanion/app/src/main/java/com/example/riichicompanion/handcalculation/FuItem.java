package com.example.riichicompanion.handcalculation;

public class FuItem {

    private String title;
    private int value;

    public FuItem(String title, int value) {
        this.title = title;
        this.value = value;
    }

    public String getTitle() {
        return title;
    }

    public int getValue() {
        return value;
    }
}
